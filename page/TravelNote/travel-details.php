    <div class="text-img p-t-lg">
        <p class="">总觉得印度菜和日本菜在烹饪的两个截然相反的方向做到了极致。日本菜讲究的食物的原味，力求用最少的外部影响展现出精挑细选的食材味道，哪怕吃的是一颗豆子，都会让人惊讶豆子原来还能有这么香的豆味。印度菜则是另一个极端，作为香料的王国，印度厨师有无数的排列组合可以选，如何在一道菜里选出最合适的十几种乃至几十种香料并且比例恰到好处，这里面有太多的学问。印度的食材挑选相对简单，对于大部分餐厅，羊就是羊，鸡就是鸡，简单粗暴，很多时候让人感觉香料才是真正的主角。当然，顶级的印度餐厅里，香料的味道不会完全掩盖食材本身，两者有一个很好的平衡。这次我的印度之行从北到南几乎一个月，当中出了一次赶清晨的飞机吃了麦当劳早餐外，剩余时间全部吃的印度菜，并且在条件允许的情况下尽量在当地有名的餐厅解决，涵盖了寺庙食堂到五星酒店中间各种档次。这篇帖子大概就是我对当地美食所得的一点经验总结。</p>
        <img src="../../Public/img/text1.gif" alt="">
        <p>宗教错综复杂，地方文化和差别很大，饮食自然有很多偏好和禁忌。在国内提起吃饭，通常想到的问题是哪个菜系，粤菜还是川菜；在北美，首先想到的一定是哪个国家的菜式，墨西哥菜式还是韩国菜；放在印度，首要的问题变成了——吃素还是吃荤......印度素食餐厅非常多，除了跟佛教和耆那教的饮食习惯有关外，印度人普遍喜欢跟动物共存，吃它们的肉不被部分人接受，即使吃肉的人也不一定每顿都吃。有的餐厅会大大的写着Veg(英语vegetarian的缩写)，有的则没有任何提示。有一个很好的办法认出素食餐厅：凡是名字跟神有关的，例如什么Shiva、Vishnu、Ganesha之类的，95%是素食的。当然这个逻辑反过来不一定好用，不是每个素食餐厅都靠神来揽客......</p>
        <p>吃肉的话选择也有限，大部分餐厅只有羊和鸡两种，那是因为穆斯林嫌猪不洁，印度教徒崇拜牛，剩下能吃的还真没多少。当然猪肉牛肉在印度还是有的，特别在班加罗尔这种平均教育程度高，外国人多的城市，即使是印度他们的信仰观念也没那么强，生活比较西化牛排照吃不误。南印近海的地方海鲜比较流行。</p>
        <img src="../../Public/img/text2.gif" alt="">
        <p class="font-bold">Filter Coffee</p>
        <p>印度给人的印象是一个茶叶大国，全民喝茶。但其实北印的人喜欢喝茶，而南印的更喜欢咖啡。卡纳塔克邦的Coorg是印度著名的咖啡产区，咖啡豆磨成粉后会被装到一个越式咖啡的金属滴漏器中，加开水稍等一会直道滴漏完成，然后按照个人口味加适量的糖煮一小锅牛奶。最后咖啡会被倒进牛奶锅中，再一起倒进另一个锅，反复好几次方告完成。印度的Filter Coffee比起欧美的Filter Coffee做起要复杂，牛奶更多。咖啡大概在16-17世纪被引入到印度，自那之后就大受欢迎。现在印度无论南北，咖啡店越来越多，从本土的Coffee Day到外国的星巴克等在大城市随处可见，直到大概100年前他们在咖啡里放的还是片糖而非现在的白糖。要喝正宗的印度Filter Coffee，还是不推荐国际连锁，最好在当地餐厅或者酒店。</p>
